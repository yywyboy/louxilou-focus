function loadOrder(): Engine[] {
  try {
    const saved = JSON.parse(localStorage.getItem(STORAGE_KEY) || '[]') as string[]
    if (saved.length === 0) return defaultEngines
    return saved.map((id) => defaultEngines.find((e) => e.id === id)).filter(Boolean) as Engine[]
  } catch {
    return defaultEngines
  }
}

const engines = ref<Engine[]>(loadOrder())
const activeId = ref(localStorage.getItem(ACTIVE_KEY) || 'google')

const activeEngine = computed(() => engines.value.find((e) => e.id === activeId.value) || engines.value[0])
const activeIndex = computed(() => engines.value.findIndex((e) => e.id === activeId.value))

function setActive(id: string) {
  activeId.value = id
  localStorage.setItem(ACTIVE_KEY, id)
}

function setActiveByIndex(i: number) {
  if (i >= 0 && i < engines.value.length) setActive(engines.value[i].id)
}

function getIcon(id: string): string {
  return engineIcons[id] || ''
}

function saveOrder() {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(engines.value.map((e) => e.id)))
}

export function useEngines() {
  return { engines, activeEngine, activeId, activeIndex, setActive, setActiveByIndex, getIcon, saveOrder }
}
